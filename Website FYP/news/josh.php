	<div class="post_title"><h1 style="text-align: center; margin-top: 69px;"> Josh’s South Downs Way challenge raises  <br> £1,700 for children’s charity </h1></div>
	<br>
    <div class="post_description"> LAST weekend Josh Braid from Hurst walked the 170km South Downs Way to raise money for Rockinghorse, pulling a tyre the whole way!</div>
	<div class="post_image">
		<img src="../images/IMAGES Important/Josh-and-Frencesca-on-Eastbourne-beach-768x576.jpg "style="width:100%" >
</div>
	<div class="post_text">Josh Braid certainly isn’t frightened of a challenge. In recent years, he has taken on a range of charity fundraisers, but he has really decided to push himself this year.<br>

  And last weekend saw his latest challenge when Josh and his wife Francesca walked the whole distance of the South Downs Way, just over 170km, from Winchester to Eastbourne. And not only that, but Josh also pulled a 12kg tyre behind him for the whole journey.<br><br>
  
  
  <strong>As Josh explained:</strong>
  
  
  “Pre-COVID I was planning to climb Europe’s highest mountain in Russia, but obviously that was cancelled. My next plan is to go to the South Pole, but as I won’t be able to do that for a while, I wanted to try and find a challenge that would help me prepare.<br><br>
  
  
  “Obviously, at the Pole, I will be skiing and pulling a sledge behind me, but I can’t really do that in the UK, so I thought that pulling a tyre might be a good replacement and help prepare me a little more for the real thing.”<br><br></div>
		
		<div class="post_image2">
			<img src="../images/IMAGES Important/Josh-and-Francesca-at-the-start-of-their-walk-scaled-e1623228341347-768x662.jpg" style="width:100%;">
	</div>

	<div class="post_text"><strong>Josh also wanted to use this challenge as a way of raising money for his local children’s charity, he said:<br><br></strong>


  “I have two young daughters, and whilst they are lucky enough not to have experienced any serious medical issues, we recognise the incredibly important work that Rockinghorse does to support families who are experiencing serious illness and going through treatment.<br><br>
  
  
  “Which is why I wanted to raise money for the charity and help them continue this support for local children.”<br><br>
  
  
  On Thursday, 3 June, Josh set off from Winchester with his wife Francesca, who was raising money for Street Vet. Despite one day of awful weather, they managed to cover around 40km, or 25 miles, each day: the equivalent to four marathon distances back-to-back!<br><br>
  
  
  So far, Josh has raised over £1,700 but is hoping for more. The company he works for have donated £500 and helped him with some of the kit for his trek but he would love to raise a few thousand pounds for Rockinghorse.<br><br>
  
  So, if you would like to sponsor Josh, all you need to do is head over to his JustGiving Page. Or, to donate to Street Vet, Francesca’s chosen charity, please visit her JustGiving page.<br><br> 
  
  Well done to Josh and Francesca on completing this mammoth task and for raising so much money for local charities.
  
  
