	<div class="post_title"><h1 style="text-align: center; margin-top: 69px;"> Celebrities to star in September’s  <br> Soccer Aid for UNICEF </h1></div>
	<br>
    <div class="post_description"> JAMES Arthur, Tom Grennan, Fara Williams, Micah Richards, Gary Neville and Paul Scholes were today announced as taking part in Soccer Aid for UNICEF 2021 – the first time they have been part of the world’s biggest celebrity-charity football match.</div>
	<div class="post_image">
		<img src="../images/IMAGES Important/related_image3.jpg "style="width:100%" >
</div>
	<div class="post_text">They will be joined by Wayne Rooney, who is coming out of retirement to play for England again. The Three Lions’ all-time record goalscorer managed the England side in 2020 but is once again taking to the pitch as a player.<br><br>


  Dermot O’Leary returns to host the live show on ITV, with England legend Alex Scott charged with getting all the gossip from the touch-line, as she makes her Soccer Aid for UNICEF presenting debut alongside him.<br><br>
  
  
  The England vs Soccer Aid World XI FC clash is the tenth-ever. A family of four can attend the anniversary game for £60. This year, for the very first time, the match takes place on a Saturday (4th September) and will be played at Manchester City’s Etihad Stadium – also a first.<br><br>
  
  
  Returning stars include Usain Bolt, Olly Murs, Mark Wright, Paddy McGuinness, Ore Oduba, Kem Cetinay, Liv Cooke, Roman Kemp, Chunkz and Maya Jama as a pundit.
  
  The line-up is further strengthened by a number of football stars, including Wayne Rooney, Gary Neville, Paul Scholes, Kelly Smith, Roberto Carlos, Patrice Evra, David James, Ashley Cole, Emile Heskey and Jamie Redknapp. Harry Redknapp returns as Manager of the Soccer Aid World XI FC, while Micah Richards joins the England coaching team with David Seaman as goalkeeping coach.<br><br>
		
		<strong>Alex Scott said: </strong><br>

“I’m so excited for this! I’ve wanted to be involved in Soccer Aid for UNICEF for many years, so I am delighted to be part of it now as a pitch-side reporter. I’ve watched the games for years, and it’s something that I know loads of people look forward to. It’ll be great to be back in a stadium with fans again – and all for such a great cause. With so many great names already signed up – and many more to come – there’s no excuse not to get your tickets now!”<br><br>


<strong>Gary Neville said: </strong><br>


“Just when I thought my days of being booed by City fans had passed! I’m hoping that my hamstrings hold out for more than five minutes, to be honest. Also, I need to get through the next few months without Micah Richards sounding off in my ear too much! All joking aside, we’re all playing for a hugely important cause – UNICEF. Their work is as important now as it has ever been, so please buy a ticket and come along – it will be great fun.”<br><br>


<strong>Micah Richards said: </strong><br>


“Usain Bolt running at Gary Neville? Rooney out of retirement? Me making my coaching debut. What more do you want?! This is going to be so good! I can’t wait to see all the lads run out at The Etihad for such a great cause. The line-up is looking really strong, and let me tell you – there are even more surprises to come!”<br><br>


<strong>Wayne Rooney said: </strong><br>


“Pulling on an England shirt is always special, so I’m absolutely delighted to be doing that again in Soccer Aid for UNICEF this September. Managing the England team last year was great but being around the players made me want to lace up the boots again – now I’ve got that chance, one last time. Soccer Aid for UNICEF has been a massive force for good since it started back in 2006, and I know the public will support us again this year by purchasing tickets, which are now on sale.”<br><br>


